        public void listarClientes(){
        
        ArrayList<String []> listaClientes = ClienteController.listar();
        
        //Passo os dados pra model
        DefaultTableModel modelo = new DefaultTableModel();
        modelo = (DefaultTableModel) tblCliente.getModel();
        
        //Zero o modelo antes de exibir para evitar duplicidades
        modelo.setRowCount(0);
        
        for (String[] listaCliente : listaClientes) {
            modelo.addRow(listaCliente);
        }
    
    }
        
    
